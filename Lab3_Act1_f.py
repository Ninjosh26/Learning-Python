print("This program converts from Fahrenheit to Celsius")
# Asks user to input degrees Fahrenheit
fahrenheit = input("Please enter the degrees Fahrenheit to be converted to degrees Celsius:")
# Converts Fahrenheit to Celsius
celsius = (float(fahrenheit) - 32) * 5 / 9
print(fahrenheit, "degrees Fahrenheit is equivalent to", celsius, "degrees Celsius")