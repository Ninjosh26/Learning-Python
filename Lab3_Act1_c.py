print("This program converts Pascals to Millimeters of Mercury")
# Asks user to input Pascals
pascals = input("Please enter the number of Pascals to be converted to Millimeters of Mercury:")
# Converts Pascals to Millimeters of Mercury
milOfMercury = float(pascals) * 0.00750062
print(pascals, "Pascals is equivalent to", milOfMercury, "Millimeters of Mercury")